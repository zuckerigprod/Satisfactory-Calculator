export function useTranslations() {
  function translatePart(name: string, locale: string): string {
    if (locale === 'en') return name
    if (locale === 'ru') {
      return partTranslations[name] ?? name
    }
    return name
  }

  function translateMachine(name: string, locale: string): string {
    if (locale === 'en') return name
    if (locale === 'ru') {
      return machineTranslations[name] ?? name
    }
    return name
  }

  function translateRecipe(name: string, locale: string): string {
    if (locale === 'en') return name
    if (locale === 'ru') {
      return recipeTranslations[name] ?? partTranslations[name] ?? name
    }
    return name
  }

  const iconFallbacks: Record<string, string> = {
    'Miner': 'Miner_Mk.1',
    'Storage Container': 'Storage_Container',
    'AWESOME Sink': 'AWESOME_Sink',
  }

  function getIconPath(name: string): string {
    const key = name.replace(/ /g, '_')
    const fallback = iconFallbacks[name]
    return `/icons/${fallback || key}.png`
  }

  return {
    translatePart,
    translateMachine,
    translateRecipe,
    getIconPath,
  }
}
